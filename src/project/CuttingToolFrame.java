    private void searchbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbuttonActionPerformed
        SearchSuspect ss = new SearchSuspect();
        ss.setVisible(true);
        
        if(InfoClass.suspectList.isEmpty()){
         ss.jTextArea1.append("\nThere are no suspect in the system");
        }
        else{
         String suspectId = (String)susIdcomboBox.getSelectedItem();
         Crime cd = InfoClass.searchSuspect(suspectId);
          if(cd == null)
                ss.jTextArea1.append("\nSuspect with the id " + suspectId + " is not found");        
            else
                ss.jTextArea1.append(cd.toString());
        }
    }//GEN-LAST:event_searchbuttonActionPerformed

