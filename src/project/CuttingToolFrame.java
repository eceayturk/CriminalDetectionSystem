     String suspect_id = susId5.getText();
     String sus_name = (String)namesuscutting.getText();
     String sus_surname = (String)sussurnamecutting.getText();
     String gender_sus = (String)gendersus4.getSelectedItem();
     int age_sus = Integer.parseInt(agesus3.getText());
     double height_sus = Double.parseDouble(hsus3.getText());
     double weight_sus = Double.parseDouble(wsus3.getText());
     String FMR;
     String sus_phone = (String)cuttingsusnum.getText();
     String weaponType;
     String bloodSample;
     String bloodSamplee;
     String hormoneLevel = (String)drowcort4.getSelectedItem();
     susIdcomboBox.addItem(suspect_id);
     int age_vic = Integer.parseInt(agevic.getText());
     double height_vic = Double.parseDouble(heightvic.getText());
     double weight_vic = Double.parseDouble(weightvic.getText());
     String[] logcalls = new String[5];
     String numberss = numbers.getText();
     logcalls = numberss.split(",");
     
     double sus_strength = PhysicalFeatures.CalcMuscleStrength(age_sus, height_sus, weight_sus);
     double vic_strength = PhysicalFeatures.CalcMuscleStrength(age_vic, height_vic, weight_vic);
     
     
     String[] widthHeightLength = new String[3];
     String areaCrime = whl.getText();
     widthHeightLength = areaCrime.split(":");
     
     
     String[] widthHeightLength2 = new String[3];
     String areaSusCrime = suscrimearea.getText();
     widthHeightLength2 = areaSusCrime.split(":");
     double susArea = (Double.parseDouble(widthHeightLength2[0])* Double.parseDouble(widthHeightLength2[1])* 
                Double.parseDouble(widthHeightLength2[2]));
     
     double cuttingAreaCrime = (Double.parseDouble(widthHeightLength[0])* Double.parseDouble(widthHeightLength[1])* 
                Double.parseDouble(widthHeightLength[2]));
     
     double cuttingAreaSuspect;
     
     if(jRadioButton27.isSelected()){
         FMR = "Not Found";
     }
     else{
         FMR = (String)fpmr6.getSelectedItem();
     }

     if(jRadioButton30.isSelected()){
       weaponType = (String)cuttingweap.getSelectedItem();
     }
     else{
       weaponType = "Not Found";
     }

     
     if(suscrimearea.getText() == "0:0:0"){
         cuttingAreaSuspect = 0;
     }
     else{
         cuttingAreaSuspect = susArea;
     }
     
     
     if (cuttingdrug.isSelected() == true && cuttingalc.isSelected() == false){
         bloodSample = "Drug found!!";
     }
     else if(cuttingalc.isSelected() == true && cuttingdrug.isSelected() == false){
         bloodSample = "Alcohol has found!!";
      }
     else if(cuttingalc.isSelected() == true && cuttingdrug.isSelected() == true){
         bloodSample = "Drug and Alcohol found!!";
     }
     else{
        bloodSample = "No foreign substance was found in the blood.";
     }
     
     if (cuttingdrug.isSelected() == true && cuttingalc.isSelected() == false){
         bloodSamplee = "Drug";
     }
     else if(cuttingalc.isSelected() == true && cuttingdrug.isSelected() == false){
         bloodSamplee = "Alcohol";
      }
     else if(cuttingdrug.isSelected() == true && cuttingalc.isSelected() == true){
         bloodSamplee = "";
     }
     else{
        bloodSamplee = " ";
     }
     
      int rateBs = 0;
      int rateFp = 0;
      int rateMs = 0;
      int rateHormone = 0;
      int rateCrimeWeapon = 0;
      int ratePhone = 0;
      int totalRate=0;
      int rateArea=0;
      
     
      rateBs = CuttingToolDeath.bloodSample(bloodSamplee);
      rateFp = CuttingToolDeath.FingerprintRate(FMR);
      rateMs = PhysicalFeatures.compareMuscleStrenght(vic_strength, sus_strength);
      rateHormone = CuttingToolDeath.hormoneLevel(hormoneLevel);
      ratePhone = CuttingToolDeath.comparePhoneNum(sus_phone, logcalls);
      rateArea = CuttingToolDeath.compareCrimeArea(cuttingAreaCrime, cuttingAreaSuspect);
      
      if (jRadioButton28.isSelected() && jRadioButton30.isSelected()){
          rateCrimeWeapon = CuttingToolDeath.crimeWeapon();
      }
      else{
          rateCrimeWeapon = 0;
    }
      
     totalRate = rateBs+rateFp+rateMs+rateHormone+rateCrimeWeapon+ratePhone+rateArea;
     
     CuttingToolDeath s = new CuttingToolDeath(suspect_id,sus_name,sus_surname,gender_sus,FMR,age_sus,height_sus,
                                         weight_sus, weaponType,sus_strength,cuttingAreaSuspect,hormoneLevel, bloodSample,sus_phone,totalRate);

    InfoClass.addSuspect(s);
    jTextField4.setText("Suspect added!!");
